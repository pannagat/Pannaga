class switchexample
{
public static void main(String args[])
{
int i;
for(i=0;i<4;i++)
switch(i)
{
case 0:
System.out.println("value of i is zero");
break;

case 1:
System.out.println("value of i is one");
break;

case 2:
System.out.println("value of i is two");
break;

case 3:
System.out.println("value of i is three");
break;

default:
System.out.println("value of i is more than 3");
break;
}
}
}
